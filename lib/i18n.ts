function normalizeLocaleToken(value: string) {
  return value.trim().split(";")[0]?.toLowerCase().replace("_", "-") || ""
}

export function resolveLocale(input?: unknown): AppLocale {
  if (typeof input !== "string" || !input.trim()) return "zh-Hans"
  const tokens = input.split(",").map(normalizeLocaleToken).filter(Boolean)
  for (const token of tokens) {
    if (localeAliases[token]) return localeAliases[token]
    const base = token.split("-")[0]
    if (base && localeAliases[base]) return localeAliases[base]
  }
  return "zh-Hans"
}

export function localeFromRequest(req: Request, body?: Record<string, unknown>) {
  return resolveLocale(
    body?.locale ||
      body?.language ||
      body?.lang ||
      req.headers.get("x-locale") ||
      req.headers.get("x-language") ||
      req.headers.get("accept-language"),
  )
}

export function t(locale: AppLocale | string | undefined, key: MessageKey, vars: Record<string, string | number | null | undefined> = {}) {
  const resolved = resolveLocale(locale)
  const template = dictionaries[resolved]?.[key] || dictionaries["zh-Hans"][key] || dictionaries.en[key] || key
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(vars[name] ?? ""))
}

export function clientLocale() {
  if (typeof navigator === "undefined") return "zh-Hans"
  return resolveLocale(navigator.languages?.join(",") || navigator.language)
}

export function localeDateCode(locale: AppLocale) {
  if (locale === "zh-Hans") return "zh-CN"
  if (locale === "zh-Hant") return "zh-TW"
  if (locale === "pt") return "pt-BR"
  if (locale === "es") return "es-ES"
  return locale
}
